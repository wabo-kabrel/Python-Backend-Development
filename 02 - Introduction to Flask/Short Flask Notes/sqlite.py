#1. What is SQLite?
# - According to the official documentation, SQLite is a C language 
#   library that implements a small, fast and a self-contain SQL 
#   database engine.
# - SQLite is the most used database engine in the world.
# - SQLite is in-built in all mobile phones and most computers, and it
#   comes bundled inside countless other applications.
# - It is a file-based database that can be used in different platforms